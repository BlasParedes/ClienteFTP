    private void DesconectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_DesconectarActionPerformed
           if( Conectado ){
                if( ftp.desconectar() ){
                    EstadoConexion.setText("Estado: No conectado");
                    JOptionPane.showMessageDialog(null, "Desconexion Exitosa");
                } else {
                    EstadoConexion.setText("Estado: No conectado");
                    JOptionPane.showMessageDialog(null, "Error al desconectar");
                }
           } else {
               JOptionPane.showMessageDialog(null, "Servidor no Conectado");
           }
    }//GEN-LAST:event_DesconectarActionPerformed

