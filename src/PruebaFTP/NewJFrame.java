    private void ConectarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ConectarActionPerformed
        if( Servidor.getText().length() > 0 && Usuario.getText().length() > 0 && Contraseña.getText().length() > 0 ){
            boolean resp = ftp.conectar(Servidor.getText(), Usuario.getText(), Contraseña.getText());
            if(resp){
                Conectado = true;
                EstadoConexion.setText("Estado: Conectado");
                JOptionPane.showMessageDialog(null, "Conexion Exitosa");
                cargarListaArchivos();
            }else{
                JOptionPane.showMessageDialog(null, "Servidor, Usuario o contraseña incorrectos.");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Datos ingresados incorrectos");
        }
    }//GEN-LAST:event_ConectarActionPerformed

    private void CambiarRutaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_CambiarRutaActionPerformed
        if( Conectado ){
            rutaDescarga = URLDescargar.getText();
            URLDescargar.setText("");
        }else{
            JOptionPane.showMessageDialog(null, "Servidor Desconectado");
        }
    }//GEN-LAST:event_CambiarRutaActionPerformed

    private void SubirArchivoActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_SubirArchivoActionPerformed
        if( Conectado ){
            if ( ftp.subirFichero( NombreFicheroZIPEnviar.getText(), URLFicheroZIPEnviar.getText() ) ){
                JOptionPane.showMessageDialog(null, "Archivo Subido con exito");
            }else{
                JOptionPane.showMessageDialog(null, "Error al Subir el archivo");
            }
        }else{
            JOptionPane.showMessageDialog(null, "Servidor Desconectado");
        }
    }//GEN-LAST:event_SubirArchivoActionPerformed

